//! [7]
tetris_piece tetris_piece::rotatedLeft() const
{
    if (pieceShape == SquareShape)
        return *this;

    tetris_piece result;
    result.pieceShape = pieceShape;
    for (int i = 0; i < 4; ++i) {
        result.setX(i, y(i));
        result.setY(i, -x(i));
    }
//! [7]
    return result;
}

//! [9]
tetris_piece tetris_piece::rotatedRight() const
{
    if (pieceShape == SquareShape)
        return *this;

    tetris_piece result;
    result.pieceShape = pieceShape;
    for (int i = 0; i < 4; ++i) {
        result.setX(i, -y(i));
        result.setY(i, x(i));
    }
//! [9]
    return result;
}
